export const CHAR_WIDTH = 5;
export const CHAR_HEIGHT = 7;
export const CHAR_SPACING = 1; // 1 cell gap between characters

/**
 * Returns the bitmap cells for a given text string.
 * Each cell is { relRow, relCol, filled } relative to the origin (0,0).
 * Only filled cells are returned.
 */
export function getTextBitmap(text: string): { relRow: number; relCol: number }[] {
  const upper = text.toUpperCase();
  const cells: { relRow: number; relCol: number }[] = [];
  let offsetCol = 0;

  for (const char of upper) {
    const glyph = FONT[char];
    if (!glyph) {
      // Unknown character → treat as space
      offsetCol += CHAR_WIDTH + CHAR_SPACING;
      continue;
    }
    for (let r = 0; r < CHAR_HEIGHT; r++) {
      for (let c = 0; c < CHAR_WIDTH; c++) {
        if (glyph[r][c]) {
          cells.push({ relRow: r, relCol: offsetCol + c });
        }
      }
    }
    offsetCol += CHAR_WIDTH + CHAR_SPACING;
  }

  return cells;
}

/** Get the total width in cells for a text string */
export function getTextWidth(text: string): number {
  if (text.length === 0) return 0;
  return text.length * (CHAR_WIDTH + CHAR_SPACING) - CHAR_SPACING;
}
